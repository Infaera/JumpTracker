    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        typeList = new ArrayList();
        parent.jumper.getTypeList().forEach((type) -> {
            if (!typeList.contains(type)) {
                typeList.add(type);
            }
        });
        parent.jumper.getActiveTypes().forEach((type) -> {
            if (!typeList.contains(type)) {
                typeList.add(type);
            }
        });
        defaultComboBoxModel1 = new DefaultComboBoxModel(typeList.toArray(new String[typeList.size()]));
        jComboBox1.setModel(defaultComboBoxModel1);
    }//GEN-LAST:event_jButton5ActionPerformed


