    //delete
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String element = (String) defaultComboBoxModel1.getSelectedItem();
        if (parent.jumper.getActiveTypes().contains(element) || element.equals("All")) {
            return;
        }
        typeList.remove(element);
        defaultComboBoxModel1.removeElement(element);
    }//GEN-LAST:event_jButton4ActionPerformed

    //add
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String element = jTextField1.getText();
        if (!typeList.contains(element)) {
            typeList.add(element);
            defaultComboBoxModel1.addElement(jTextField1.getText());
            jTextField1.setText("");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

